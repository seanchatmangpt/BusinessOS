-- =============================================================================
-- OSA MODULES REGISTRY
-- Tracks all OSA-generated modules and applications
-- =============================================================================
CREATE TABLE IF NOT EXISTS osa_modules (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Identification
    name VARCHAR(255) NOT NULL,
    display_name VARCHAR(255) NOT NULL,
    description TEXT,
    module_type VARCHAR(50) NOT NULL, -- 'builtin', 'generated', 'custom'

    -- Schema definitions (JSONB for flexibility)
    schema_definition JSONB, -- Database schema (tables, fields, relations)
    api_definition JSONB,    -- API endpoints (REST routes, params, responses)
    ui_definition JSONB,     -- Frontend components (Svelte files, routes)

    -- Ownership and tracking
    created_by VARCHAR(255) REFERENCES "user"(id) ON DELETE SET NULL,
    workspace_id UUID, -- Links to osa_workspaces (added below)

    -- Status
    status VARCHAR(50) DEFAULT 'draft', -- 'draft', 'active', 'archived', 'failed'
    version VARCHAR(50) DEFAULT '1.0.0',

    -- Metadata
    metadata JSONB DEFAULT '{}',
    tags TEXT[], -- For categorization and search

    -- Timestamps
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    deployed_at TIMESTAMPTZ,

    -- Indexes
    CONSTRAINT osa_modules_name_workspace_unique UNIQUE(name, workspace_id)
);

CREATE INDEX idx_osa_modules_workspace ON osa_modules(workspace_id);
CREATE INDEX idx_osa_modules_created_by ON osa_modules(created_by);
CREATE INDEX idx_osa_modules_status ON osa_modules(status);
CREATE INDEX idx_osa_modules_type ON osa_modules(module_type);
CREATE INDEX idx_osa_modules_tags ON osa_modules USING GIN(tags);

-- =============================================================================
-- OSA WORKSPACES
-- User workspaces with 2D/3D configuration
-- =============================================================================
CREATE TABLE IF NOT EXISTS osa_workspaces (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Ownership
    user_id VARCHAR(255) NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
    name VARCHAR(255) NOT NULL,

    -- Display mode
    mode VARCHAR(20) DEFAULT '2d', -- '2d', '3d', 'hybrid'

    -- Layout configuration (panel positions, connections)
    layout JSONB DEFAULT '{}',

    -- Active modules (array of module IDs)
    active_modules UUID[] DEFAULT '{}',

    -- Template type
    template_type VARCHAR(50) DEFAULT 'business_os', -- 'business_os', 'agency_os', 'content_os', 'sales_os', 'custom_os'

    -- Settings
    settings JSONB DEFAULT '{}',

    -- Timestamps
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    last_accessed_at TIMESTAMPTZ DEFAULT NOW(),

    CONSTRAINT osa_workspaces_user_name_unique UNIQUE(user_id, name)
);

CREATE INDEX idx_osa_workspaces_user ON osa_workspaces(user_id);
CREATE INDEX idx_osa_workspaces_template ON osa_workspaces(template_type);
CREATE INDEX idx_osa_workspaces_mode ON osa_workspaces(mode);

-- Add workspace_id foreign key to osa_modules now that table exists
ALTER TABLE osa_modules
ADD CONSTRAINT fk_osa_modules_workspace
FOREIGN KEY (workspace_id)
REFERENCES osa_workspaces(id)
ON DELETE CASCADE;

-- =============================================================================
-- OSA GENERATED APPS
-- Tracks full-stack applications generated by OSA-5
-- =============================================================================
CREATE TABLE IF NOT EXISTS osa_generated_apps (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Association
    workspace_id UUID NOT NULL REFERENCES osa_workspaces(id) ON DELETE CASCADE,
    module_id UUID REFERENCES osa_modules(id) ON DELETE SET NULL,

    -- App details
    name VARCHAR(255) NOT NULL,
    display_name VARCHAR(255) NOT NULL,
    description TEXT,

    -- OSA-5 workflow information
    osa_workflow_id VARCHAR(255), -- OSA-5 workflow ID
    osa_sandbox_id VARCHAR(255),  -- E2B sandbox ID

    -- Code storage
    code_repository TEXT, -- Git repo URL or file path
    deployment_url TEXT,  -- Live deployment URL

    -- Status tracking
    status VARCHAR(50) DEFAULT 'generated', -- 'generating', 'generated', 'deploying', 'deployed', 'running', 'stopped', 'failed'

    -- Generation results
    files_created INTEGER DEFAULT 0,
    tests_passed BOOLEAN DEFAULT false,
    build_status VARCHAR(50), -- 'success', 'failed', 'pending'

    -- Metadata from OSA-5
    metadata JSONB DEFAULT '{}',

    -- Error tracking
    error_message TEXT,
    error_stack TEXT,

    -- Timestamps
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    generated_at TIMESTAMPTZ,
    deployed_at TIMESTAMPTZ,
    last_build_at TIMESTAMPTZ
);

CREATE INDEX idx_osa_apps_workspace ON osa_generated_apps(workspace_id);
CREATE INDEX idx_osa_apps_module ON osa_generated_apps(module_id);
CREATE INDEX idx_osa_apps_status ON osa_generated_apps(status);
CREATE INDEX idx_osa_apps_workflow ON osa_generated_apps(osa_workflow_id);

-- =============================================================================
-- OSA EXECUTION HISTORY
-- Tracks all code executions via terminal
-- =============================================================================
CREATE TABLE IF NOT EXISTS osa_execution_history (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Association
    user_id VARCHAR(255) NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
    app_id UUID REFERENCES osa_generated_apps(id) ON DELETE SET NULL,
    workspace_id UUID REFERENCES osa_workspaces(id) ON DELETE CASCADE,

    -- Execution details
    command TEXT NOT NULL,
    working_directory TEXT,
    environment_vars JSONB DEFAULT '{}',

    -- Results
    output TEXT,
    error_output TEXT,
    exit_code INTEGER,

    -- Performance
    duration_ms INTEGER,

    -- Context
    triggered_by VARCHAR(50), -- 'user', 'agent', 'workflow', 'cron'
    metadata JSONB DEFAULT '{}',

    -- Timestamp
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_osa_exec_user ON osa_execution_history(user_id);
CREATE INDEX idx_osa_exec_app ON osa_execution_history(app_id);
CREATE INDEX idx_osa_exec_workspace ON osa_execution_history(workspace_id);
CREATE INDEX idx_osa_exec_created ON osa_execution_history(created_at DESC);

-- =============================================================================
-- OSA SYNC STATUS
-- Tracks sync state between BusinessOS and OSA-5
-- =============================================================================
CREATE TABLE IF NOT EXISTS osa_sync_status (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Sync target
    entity_type VARCHAR(50) NOT NULL, -- 'user', 'workspace', 'app', 'module'
    entity_id UUID NOT NULL,

    -- OSA-5 reference
    osa_entity_id VARCHAR(255), -- ID in OSA-5 system
    osa_entity_type VARCHAR(50), -- 'project', 'workspace', 'user'

    -- Sync state
    sync_status VARCHAR(50) DEFAULT 'pending', -- 'pending', 'syncing', 'synced', 'failed', 'conflict'
    last_sync_at TIMESTAMPTZ,
    next_sync_at TIMESTAMPTZ,

    -- Sync direction
    sync_direction VARCHAR(50) DEFAULT 'bidirectional', -- 'to_osa', 'from_osa', 'bidirectional'

    -- Error tracking
    error_count INTEGER DEFAULT 0,
    last_error TEXT,

    -- Data snapshot (for conflict resolution)
    local_snapshot JSONB,
    remote_snapshot JSONB,

    -- Metadata
    metadata JSONB DEFAULT '{}',

    -- Timestamps
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),

    CONSTRAINT osa_sync_entity_unique UNIQUE(entity_type, entity_id)
);

CREATE INDEX idx_osa_sync_entity ON osa_sync_status(entity_type, entity_id);
CREATE INDEX idx_osa_sync_status ON osa_sync_status(sync_status);
CREATE INDEX idx_osa_sync_next ON osa_sync_status(next_sync_at);

-- =============================================================================
