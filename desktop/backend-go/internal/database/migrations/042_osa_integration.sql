-- osa_modules updated_at trigger
CREATE OR REPLACE FUNCTION update_osa_modules_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

